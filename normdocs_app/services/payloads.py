from __future__ import annotations


def payload_form_from_normatives(normative_corpus: str) -> str:
    """Вход для потока 1: построение формы отчёта по нормативным документам."""
    return (
        "Задача: на основе приведённого корпуса нормативных документов составь структурированную "
        "форму отчёта (шаблон): разделы, поля, пояснения что в каждое поле вносить.\n"
        "Ответ на русском языке. Используй маркдаун-заголовки и списки.\n\n"
        "--- НОРМАТИВНЫЕ ДОКУМЕНТЫ (фрагмент) ---\n"
        f"{normative_corpus.strip()}"
    )


def payload_fill_template(report_template: str, region_data_corpus: str) -> str:
    """Вход для потока 2: заполнение формы данными региона."""
    return (
        "Задача: заполни приведённую форму отчёта фактическими данными из второго блока. "
        "Сохрани структуру формы; если данных для поля нет — укажи «н/д» или краткое пояснение.\n"
        "Ответ на русском языке — готовый заполненный отчёт целиком.\n\n"
        "--- ФОРМА ОТЧЁТА ---\n"
        f"{report_template.strip()}\n\n"
        "--- ВХОДНЫЕ ДАННЫЕ (регион / вводные) ---\n"
        f"{region_data_corpus.strip()}"
    )


def payload_verify(normative_corpus: str, filled_report: str) -> str:
    """Вход для потока 3: проверка отчёта на соответствие нормативке."""
    return (
        "Задача: проверь заполненный отчёт на соответствие требованиям из нормативных документов. "
        "Укажи: (1) соответствия, (2) несоответствия и риски, (3) недостающие сведения, "
        "(4) краткое заключение.\n"
        "Ответ на русском языке, структурированный списком.\n\n"
        "--- НОРМАТИВНЫЕ ДОКУМЕНТЫ (фрагмент) ---\n"
        f"{normative_corpus.strip()}\n\n"
        "--- ЗАПОЛНЕННЫЙ ОТЧЁТ ---\n"
        f"{filled_report.strip()}"
    )
